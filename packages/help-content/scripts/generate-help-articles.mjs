import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import matter from "gray-matter";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const markdownDir = path.resolve(__dirname, "../markdown");
const outFile = path.resolve(__dirname, "../src/generated/help-articles.ts");

function parseTags(value) {
  if (Array.isArray(value)) {
    return value.map(String).map((tag) => tag.trim()).filter(Boolean);
  }
  if (typeof value === "string") {
    return value.split(",").map((tag) => tag.trim()).filter(Boolean);
  }
  return [];
}

function parseOrder(value) {
  if (typeof value === "number" && Number.isFinite(value)) return value;
  if (typeof value === "string") {
    const parsed = Number.parseInt(value, 10);
    if (Number.isFinite(parsed)) return parsed;
  }
  return 100;
}

function parseUpdated(value) {
  if (value instanceof Date) {
    return value.toISOString().slice(0, 10);
  }
  return typeof value === "string" ? value : "";
}

function normalizeArticle(slug, data, content) {
  const title = typeof data.title === "string" ? data.title : slug;
  const description = typeof data.description === "string" ? data.description : "";
  const category = typeof data.category === "string" ? data.category : "Help";
  const tags = parseTags(data.tags);
  return {
    slug,
    title,
    description,
    category,
    tags,
    updated: parseUpdated(data.updated),
    order: parseOrder(data.order),
    content,
  };
}

function literalString(s) {
  return JSON.stringify(s);
}

if (!fs.existsSync(markdownDir)) {
  console.warn(`generate-help-articles: missing ${markdownDir}, writing empty bundle`);
  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  fs.writeFileSync(
    outFile,
    `/** Generated by scripts/generate-help-articles.mjs */
export const ALL_HELP_ARTICLES = [] as const;
`
  );
  process.exit(0);
}

const articles = fs
  .readdirSync(markdownDir)
  .filter((name) => name.endsWith(".md"))
  .map((fileName) => {
    const slug = fileName.replace(/\.md$/, "");
    const source = fs.readFileSync(path.join(markdownDir, fileName), "utf8");
    const parsed = matter(source);
    return normalizeArticle(slug, parsed.data, parsed.content.trim());
  })
  .sort((a, b) => a.order - b.order || a.title.localeCompare(b.title));

const body = articles
  .map(
    (a) =>
      `  {\n    slug: ${literalString(a.slug)},\n    title: ${literalString(a.title)},\n    description: ${literalString(a.description)},\n    category: ${literalString(a.category)},\n    tags: ${JSON.stringify(a.tags)},\n    updated: ${literalString(a.updated)},\n    order: ${a.order},\n    content: ${literalString(a.content)},\n  }`
  )
  .join(",\n");

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(
  outFile,
  `/** Generated by scripts/generate-help-articles.mjs. Run: pnpm --filter @cashsouk/help-content generate */
export const ALL_HELP_ARTICLES = [
${body}
] as const;
`
);

console.log(`generate-help-articles: wrote ${articles.length} articles to src/generated/help-articles.ts`);
